// Qs4. Write a JavaScript program to test whether the character at the given (character) 
// index is lower case.

let str = "AmAn KashYap";
let ind = 3;

if (str[ind] == str[ind].toLowerCase()) {
    console.log("Character at index", ind, "is lower case");
}
else {
    console.log("Character at index", ind, "is not  lower case");
}

