// queryselector : alternate of getelement selector apply for each

document.querySelector('h1');
// select the first h1 tag

document.querySelector('.oldImg');
// select the first element with class oldImg

document.querySelector('#mainImg');
// select the first element with id mainImg

// it only select the first element so be care with that

// we want anchor tag under box
document.querySelector('.box a');
// select the first anchor tag under box

// to get all value
document.querySelectorAll('.box a');
// select all anchor tag under box
// it will return node list